function select(id){
    for(var i=0;i<=5;i++) $("#"+i).removeClass("select");
    $("#"+id).addClass("select")
    selected = id
}

function next(){
    if (quests_order == 15 || wrong == 3) stopQuests()
    else {
        if(selected == 0) notify("Nu ai selectat nimic!")
        else {
            if(type == 0) {type = selected;$("body").append("<div class='info'>Parcurs: "+quests_order+"/15<br>Corecte: "+correct+"/15<br>Gresite: "+wrong+"</div>")}
            else {
                if (selected == quests[type][quests_order]["answerCorrect"]) correct++
                else wrong++
                quests_order++
                $(".info").remove()
                $("body").append("<div class='info'>Parcurs: "+quests_order+"/15<br>Corecte: "+correct+"/15<br>Gresite: "+wrong+"/3</div>")
            }
            startQuests(selected)
        }
    }
}

function stopQuests(){
    $(".warp").remove()
    $(".quest").remove()
    $(".button").remove()
    $(".info").remove()
    if (quests_order == 15) {notify("Ai terminat testul, acum vei da proba practica!");$.post('http://vrp_licenses/close', JSON.stringify({failed: false,vehicle: quests[type]["vehicle"],type: type}))}
    if (wrong == 3) {notify("Ai picat testul teoretic...");$.post('http://vrp_licenses/close', JSON.stringify({failed: true,vehicle: quests[type]["vehicle"],type: type}))}
}

function startQuests(){
    $(".warp").remove()
    $(".quest").remove()
    $(".button").remove()
    $("body").append("<div class='quest'>"+quests[type][quests_order]["question"]+"</div>")
    for(var i=1;i<=3;i++)
        $("body").append("<div id='"+i+"' class='warp' onclick = 'select("+i+")'>"+quests[type][quests_order][i]+"</div>")
    $("body").append("<div class='button' onclick = 'next()'>Continua</div>")
    selected = 0
}

function notify(text){
    $.post('http://vrp_licenses/notify', JSON.stringify({text: text}));
}

